#include <iostream>

using namespace std;

int main()
{
    int a,b;
    a = 10;
    b = 8;

    cout << "== Menukar Nilai Kedua Variable ==" << endl;
    cout << "------------------------------" << endl;
    cout << "       [Nilai Variable]" << endl << endl;
    cout << "Angka Pertama = " << a << endl;
    cout << "Angka Kedua = " << b << endl;
    cout << endl;
    cout << "------------------------------" << endl;
    cout << "     [Hasil Pertukaran]" << endl << endl;
    cout << "Angka Pertama = " << b << endl;
    cout << "Angka Kedua = " << a << endl << endl;
    cout << "------------------------------" << endl;


    return 0;
}
